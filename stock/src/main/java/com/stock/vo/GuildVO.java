package com.stock.vo;

import java.util.Date;

public class GuildVO {
	private String cmpyName;
	private String product;
	private String tel;
	private String banNo;
	private String dataSource;
	private String dataSourceUrl;
	private Date updateDate;
	private String website;
	private String email;
	private String staffCount;
	
	public final String getCmpyName() {
		return cmpyName;
	}
	public final void setCmpyName(String cmpyName) {
		this.cmpyName = cmpyName;
	}
	public final String getProduct() {
		return product;
	}
	public final void setProduct(String product) {
		this.product = product;
	}
	public final String getTel() {
		return tel;
	}
	public final void setTel(String tel) {
		this.tel = tel;
	}
	public final String getBanNo() {
		return banNo;
	}
	public final void setBanNo(String banNo) {
		this.banNo = banNo;
	}
	public final String getDataSource() {
		return dataSource;
	}
	public final void setDataSource(String dataSource) {
		this.dataSource = dataSource;
	}
	public final Date getUpdateDate() {
		return updateDate;
	}
	public final void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}
	public final String getDataSourceUrl() {
		return dataSourceUrl;
	}
	public final void setDataSourceUrl(String dataSourceUrl) {
		this.dataSourceUrl = dataSourceUrl;
	}
	public final String getWebsite() {
		return website;
	}
	public final void setWebsite(String website) {
		this.website = website;
	}
	public final String getEmail() {
		return email;
	}
	public final void setEmail(String email) {
		this.email = email;
	}
	public final String getStaffCount() {
		return staffCount;
	}
	public final void setStaffCount(String staffCount) {
		this.staffCount = staffCount;
	}
	
	
}
