package com.stock.vo;

public class PubftsVO {
	private String statistcDate;
	private int total;
	private int queryTimes;
	private int detailTimes;
	private String remark;
	private String createTime;
	private String sugEmail;
	private String sugCont;
	private int seqNo;
	
	public final String getStatistcDate() {
		return statistcDate;
	}
	public final void setStatistcDate(String statistcDate) {
		this.statistcDate = statistcDate;
	}
	public final int getTotal() {
		return total;
	}
	public final void setTotal(int total) {
		this.total = total;
	}
	public final int getQueryTimes() {
		return queryTimes;
	}
	public final void setQueryTimes(int queryTimes) {
		this.queryTimes = queryTimes;
	}
	public final int getDetailTimes() {
		return detailTimes;
	}
	public final void setDetailTimes(int detailTimes) {
		this.detailTimes = detailTimes;
	}
	public final String getRemark() {
		return remark;
	}
	public final void setRemark(String remark) {
		this.remark = remark;
	}
	public final String getCreateTime() {
		return createTime;
	}
	public final void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public final String getSugEmail() {
		return sugEmail;
	}
	public final void setSugEmail(String sugEmail) {
		this.sugEmail = sugEmail;
	}
	public final String getSugCont() {
		return sugCont;
	}
	public final void setSugCont(String sugCont) {
		this.sugCont = sugCont;
	}
	public final int getSeqNo() {
		return seqNo;
	}
	public final void setSeqNo(int seqNo) {
		this.seqNo = seqNo;
	}
	
}
